//Arrow function with no argumet no return value
let show = ()=>{
    console.log("Arrow Function");
}
show();

//Arrow function with arguments no return value
let add = (num1,num2)=>{
console.log(num1 + num2);
}
add(10,20);

//Arrow function with arguments and return value

let multiply = (num1,num2)=>{
return num1*num2;
}
let result = multiply(5,2);
console.log(result);